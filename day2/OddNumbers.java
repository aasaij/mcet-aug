import java.util.Scanner;
//Program to print odd numbers from 1 to N
public class OddNumbers{
	public static void main(String[] args){
		Scanner in = new Scanner(System.in);
		int n;
		n = in.nextInt();
		for (int i = 1; i<=n; i++){
			if (i%2==1)
				System.out.println(i);
		}
	}
}

